public class BeanHarmonica {
    private int codigo;
    private int codHarmonica;
    private float sen;
    private float cos;

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }
        
    public int getCodHarmonica() {
        return codHarmonica;
    }

    public void setCodHarmonica(int codHarmonica) {
        this.codHarmonica = codHarmonica;
    }

    public float getSen() {
        return sen;
    }

    public void setSen(float sen) {
        this.sen = sen;
    }

    public float getCos() {
        return cos;
    }

    public void setCos(float cos) {
        this.cos = cos;
    }        
}
